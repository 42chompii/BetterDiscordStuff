function downloadFile(downloadUrl) {
    fetch(downloadUrl)
        .then(response => response.blob())
        .then(blob => {
            const link = document.createElement('a');
            link.href = window.URL.createObjectURL(blob);
            link.download = downloadUrl.split('/').pop();
            link.click();
        });
}

